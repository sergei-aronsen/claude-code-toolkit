#!/bin/bash
# scripts/generate-skills-catalog.sh — derive templates/skills-catalog.json
# from manifest.json:.skills_pins.
#
# Why a parallel catalog file? `manifest.json` is the operational single
# source of truth for sync + drift detection (large, full schema, includes
# upstream-unknown entries and rich metadata). `skills-catalog.json` is a
# compact declarative form designed for catalog-mode consumers that want
# a flat `[{name, upstream, commit, path?, install_cmd}]` array — e.g.,
# an alternative installer that calls `claude-skills install <upstream>`
# without ever mirroring content.
#
# P10 architecture note (logic audit 2026-05-18): the toolkit keeps the
# mirror under templates/skills-marketplace/ as primary (offline install,
# version-locked content) and surfaces the catalog as a secondary
# declarative artifact so future catalog-mode consumers do not need to
# parse the full manifest schema.
#
# Output: templates/skills-catalog.json (overwritten in-place).
# Exit 0 on success; 1 on missing manifest or jq.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
MANIFEST="${REPO_ROOT}/manifest.json"
CATALOG="${REPO_ROOT}/templates/skills-catalog.json"

[ -f "$MANIFEST" ] || { echo "manifest.json missing: $MANIFEST" >&2; exit 1; }
command -v jq >/dev/null 2>&1 || { echo "jq required" >&2; exit 1; }

# Derive a flat catalog. Skip no-upstream-found entries — they have no
# upstream to install from, so a catalog consumer cannot use them.
python3 - "$MANIFEST" "$CATALOG" <<'PY'
import json, sys, datetime

manifest_path, catalog_path = sys.argv[1], sys.argv[2]
m = json.load(open(manifest_path))
pins = m.get("skills_pins", {})

entries = []
for name in sorted(pins.keys()):
    p = pins[name]
    if p.get("_status") != "active":
        continue
    repo = p.get("repo")
    commit = p.get("commit")
    path = p.get("path")
    sha256 = p.get("sha256")
    if not (repo and commit):
        continue
    entry = {
        "name": name,
        "upstream": repo,
        "commit": commit,
    }
    if path:
        entry["path"] = path
    if sha256:
        entry["sha256"] = sha256
    # install_cmd is a hint, not a contract — catalog consumers may
    # invoke this verbatim or use their own resolver.
    if path:
        entry["install_cmd"] = (
            f"claude-skills install --from-github {repo}"
            f" --commit {commit} --path {path}"
        )
    else:
        entry["install_cmd"] = (
            f"claude-skills install --from-github {repo} --commit {commit}"
        )
    entries.append(entry)

catalog = {
    "schema_version": 1,
    "generated_from": "manifest.json:skills_pins",
    "generated_at": datetime.date.today().isoformat(),
    "toolkit_version": m.get("version", ""),
    "skills_count": len(entries),
    "skills": entries,
    "_note": (
        "Auto-generated by scripts/generate-skills-catalog.sh. "
        "Do not edit by hand. Source of truth is manifest.json:skills_pins. "
        "This catalog is a compact declarative view for catalog-mode "
        "consumers; the toolkit's primary distribution remains the mirror "
        "under templates/skills-marketplace/. no-upstream-found pins "
        "(e.g., memo-skill) are intentionally omitted."
    ),
}

with open(catalog_path, "w") as f:
    json.dump(catalog, f, indent=2)
    f.write("\n")

print(f"wrote {catalog_path} ({len(entries)} skills)")
PY
